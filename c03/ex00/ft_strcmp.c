int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int result;

	i = 0;
	while (*(s1 + i) != '\0')
	{
		if (*(s1 + i) == *(s2 + i))
		{
		}
		else
		{
			result = *(s1 + i) - *(s2 + i);
			return (result);
		}
		i++;
	}
	if (*(s2 + i) != '\0')
	{
		return (0 - *(s2 + i));
	}
	return (0);
}
