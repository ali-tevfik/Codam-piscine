int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z') ||
				(*(str) >= 'A' && *(str) <= 'Z'))
		{
		}
		else
		{
			i++;
		}
		++str;
	}
	if (i != 0)
		return (0);
	return (1);
}
